export type Difficulty = 'easy' | 'medium' | 'hard';
export type PuzzleNode = { id: number; x: number; y: number };
export type PuzzleEdge = [number, number];
export type PuzzleLevel = { id: number; title: string; difficulty: Difficulty; nodes: PuzzleNode[]; edges: PuzzleEdge[] };

type Template = { difficulty: Difficulty; nodes: [number, number][]; edges: PuzzleEdge[] };
type TemplateParts = { cycles?: number[][]; paths?: number[][]; edges?: PuzzleEdge[] };

const edgesFromCycle = (cycle: number[]) => cycle.map((node, index) => [node, cycle[(index + 1) % cycle.length]] as PuzzleEdge);
const edgesFromPath = (path: number[]) => path.slice(0, -1).map((node, index) => [node, path[index + 1]] as PuzzleEdge);

const createTemplate = (difficulty: Difficulty, nodes: [number, number][], parts: TemplateParts): Template => ({
  difficulty,
  nodes,
  edges: [
    ...(parts.edges ?? []),
    ...(parts.cycles ?? []).flatMap(edgesFromCycle),
    ...(parts.paths ?? []).flatMap(edgesFromPath),
  ],
});

const buildTemplate = (difficulty: Difficulty, nodes: [number, number][], cycles: number[][]): Template =>
  createTemplate(difficulty, nodes, { cycles });

