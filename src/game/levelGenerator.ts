const keyOf = ([a, b]: PuzzleEdge) => `${Math.min(a, b)}-${Math.max(a, b)}`;

const mapNodes = (points: [number, number][]) => {
  const xs = points.map(([x]) => x);
  const ys = points.map(([, y]) => y);
  const minX = Math.min(...xs);
  const maxX = Math.max(...xs);
  const minY = Math.min(...ys);
  const maxY = Math.max(...ys);
  const width = Math.max(maxX - minX, 1);
  const height = Math.max(maxY - minY, 1);
  const inset = 0.14;
  const usable = 0.72;
  return points.map(([x, y], index) => ({
    id: index + 1,
    x: Number((inset + ((x - minX) / width) * usable).toFixed(3)),
    y: Number((inset + ((y - minY) / height) * usable).toFixed(3)),
  }));
};

const isConnected = (nodes: PuzzleNode[], edges: PuzzleEdge[]) => {
  const seen = new Set<number>([nodes[0]?.id]);
  const walk = Array.from(seen);
  while (walk.length) {
    const node = walk.pop()!;
    edges.forEach(([a, b]) => {
      const next = a === node ? b : b === node ? a : null;
      if (next && !seen.has(next)) {
        seen.add(next);
        walk.push(next);
      }
    });
  }
  return seen.size === nodes.length;
};

const assertEulerGraph = (level: PuzzleLevel) => {
  const degree = level.edges.reduce<Record<number, number>>((map, [a, b]) => ({ ...map, [a]: (map[a] ?? 0) + 1, [b]: (map[b] ?? 0) + 1 }), {});
  const odd = Object.values(degree).filter((value) => value % 2 === 1).length;
  const uniqueEdges = new Set(level.edges.map(keyOf)).size === level.edges.length;
  if (!level.nodes.length || !uniqueEdges || !isConnected(level.nodes, level.edges) || ![0, 2].includes(odd)) {
    throw new Error(`Invalid Euler level ${level.id}`);
  }
};

export const generateLevels = (count = 50): PuzzleLevel[] =>
  Array.from({ length: count }, (_, index) => {
    const id = index + 1;
    const template = levelTemplates[index];
    if (!template) throw new Error(`Missing template for level ${id}`);
    const level: PuzzleLevel = {
      id,
      title: `${template.difficulty[0].toUpperCase()}${template.difficulty.slice(1)} ${id}`,
      difficulty: template.difficulty,
      nodes: mapNodes(template.nodes),
      edges: template.edges,
    };
    assertEulerGraph(level);
    return level;
  });

export const generateLevelData = (count = 50) => generateLevels(count).map(({ nodes, edges }) => ({ nodes, edges }));
